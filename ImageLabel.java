import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * Turns a given image into an Actor.
 * 
 * @author Julia 
 * @version June 2025
 */
public class ImageLabel extends Actor
{
    /**
     * Pass in an image to represent this Actor.
     * 
     * @param image     Desired image for Actor.
     */
    public ImageLabel(GreenfootImage image)
    {
        setImage(image);
    }
}
